package calculadoraTest;


public class Calculadora {
	
	public int somar(int a, int b) {
		
		return a + b;
	}
	
	public int sub(int a, int b) {
		
		return a - b;
	}
	
	public int mult(int a, int b) {
		
		return a * b;
	}
	
	public float div( float a, float b) {
		
		return a / b;
	}
}
