package calculadoraTest;

public class CalculadoraFloat {
	
	public float somar(float a, float b) {
		
		return a + b;
	}
	
	public float sub(float a, float b) {
		
		return a - b;
	}
	
	public float mult(float a, float b) {
		
		return a * b;
	}
	
	public float div(float a, float b) {
		
		return a / b;
	}
}
